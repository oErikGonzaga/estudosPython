# Operadores Matemáticos

x = 2
y = 2.5

# soma de x e y
soma = x + y

# diferença de x e y
sub = x - y

# produto de x e y
mult = x * y

# Quociente de x e y
div = x / y

# Parte inteira de x e y
inteira = x // y

# resto de x dividido por y
resto = x % y

# x elevado a y
elev = x ** y

# x elevado a y
pot = pow(x, y)

print(soma)
print(sub)
print(mult)
print(div)
print(inteira)
print(resto)
print(elev)
print(pot)